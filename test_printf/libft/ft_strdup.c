#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*str;
	int		i;

	if (s1 == NULL)
		return (NULL);
	if (!(str = (char *)malloc(ft_strlen(s1) + 1)))
		return (NULL);
	i = -1;
	while (s1[++i])
		str[i] = s1[i];
	str[i] = '\0';
	return (str);
}
